package com.example.SchoolManagementSystem.model;

public class School {
	
	private int id;
	
	private String name;
	
	private String Add;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAdd() {
		return Add;
	}

	public void setAdd(String add) {
		Add = add;
	}
	
	

}
